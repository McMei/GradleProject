/**
 * This is an interface for Multimedia Control.
 *
 * @author Michelle Quan
 */
public interface MultimediaControl {

    /**
     * Prints out "Playing".
     */
    void play();

    /**
     * Prints out "Stopped Playing".
     */
    void stop();

    /**
     * Prints out "Previous".
     */
    void previous();

    /**
     * Prints out "Next".
     */
    void next();
}

